#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	int		s_len;
	char	*dest;

	if (!s)
		return (NULL);
	i = 0;
	s_len = ft_strlen((char *)s);
	dest = (char *)malloc(1 + s_len * sizeof(char));
	if (!dest)
		return (NULL);
	while (s[i])
	{
		dest[i] = (*f)(i, s[i]);
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
